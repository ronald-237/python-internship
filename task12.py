import json
#dictionary
print(json.dumps({"Name":"Nelson","dosh":20}))
#list
print(json.dumps(["raj","Krish"]))
#string
print(json.dumps(("aruna","john")))
#tuple
print(json.dumps("ruby"))
#integer
print(json.dumps(80))
#float
print(json.dumps(25.1))
#True
print(json.dumps(True))
#False
print(json.dumps(False))
#None
print(json.dumps(None))
